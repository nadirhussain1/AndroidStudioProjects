package com.prayertimes.qibla.appsourcehub.model;


public class SurasBean {
	public String sura, name_arabic, name_transliteration;

	public String getSura() {
		return sura;
	}

	public void setSura(String sura) {
		this.sura = sura;
	}

	public String getName_arabic() {
		return name_arabic;
	}

	public void setName_arabic(String name_arabic) {
		this.name_arabic = name_arabic;
	}

	public String getName_transliteration() {
		return name_transliteration;
	}

	public void setName_transliteration(String name_transliteration) {
		this.name_transliteration = name_transliteration;
	}

	
}
