package com.prayertimes.qibla.appsourcehub.model;


public class Messages
{


    public String meaning;
    public String title;

    public Messages()
    {
    }



    public String getMeaning()
    {
        return meaning;
    }

    public String getTitle()
    {
        return title;
    }

  

    public void setMeaning(String s)
    {
        meaning = s;
    }

    public void setTitle(String s)
    {
        title = s;
    }
}
